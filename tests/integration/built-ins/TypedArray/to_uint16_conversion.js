/*---
description: ToUint16 conversion, exercised through Uint16Array element assignment.
---*/

// Assigning to a Uint16Array element applies ToUint16.
const ta = new Uint16Array(1);
function convert(x) {
  ta[0] = x;
  return ta[0];
}

